package enums;
/**
 * Enum que establece las distintas posiciones que tiene un jugador
 * @author juaan
 *
 */
public enum Posicion {

	PORTERO, //Esta posicion es la del portero
	DEFENSA, //Esta posición hace referencia a la parte de la defensa
	CENTROCAMPISTA, //Esta posición hace referencia a la parte del centro del campo, los centrocampistas
	DELANTERO //Esta posición hace referencia a la parte de ataque del campo, los delanteros
}
